<script setup lang="ts">
const N = 57;
const isPrime = (n: number) => {
    if(n <= 1) return false;
    if(n === 2) return true;
    if(n % 2 === 0) return false;
    
    for(let i = 3; i * i <= n; i += 2) {
        if(n % i === 0) return false;
    }
    
    return true;
};

console.log(isPrime(N) ? 'YES' : 'NO');
</script>